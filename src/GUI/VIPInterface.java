    public void setObserver(VIPSubscribedObserver observer){
        this.observer = observer;
        NombreClase.setText(observer.getClass().getSimpleName());
        Valor.setText(String.valueOf(observer.getValor()));

        repaint();
        revalidate();
    }

    public void actualizarVista(){
        Valor.setText(String.valueOf(observer.getValor()));
        
        repaint();
        revalidate();
    }
    
    public void setValorObservable(double valor){
        observer.setValorObservable(valor);
    }

